public class Grade
{
 public static void main(String args[])
 {
  int temp;
  temp=89;
  if (temp>90) System.out.println("You have got : O");
  else if (temp>=81 && temp<=90) System.out.println("You have got : E");
  else if (temp>=71 && temp<=80) System.out.println("You have got : A");
  else if (temp>=61 && temp<=70) System.out.println("You have got : B");
  else if (temp>=51 && temp<=60) System.out.println("You have got : C");
  else System.out.println("You have got : F");
}
}
	  